      <h2>ABSTRACT</h2>
      
      <p> Detecting node failures in mobile wireless networks is very challenging because the network topology can be highly dynamic, the network may not be always connected, and the resources are limited. In this paper, we take a probabilistic approach and propose two node failure detection schemes that systematically combine localized monitoring, location estimation and node collaboration. Extensive simulation results in both connected and disconnected networks demonstrate that our schemes achieve high failure detection rates (close to an upper bound) and low false positive rates, and incur low communication overhead. Compared to approaches that use centralized monitoring, our approach has up to 80% lower communication overhead, and only slightly lower detection rates and slightly higher false positive rates. In addition, our approach has the advantage that it is applicable to both connected and disconnected networks while centralized monitoring is only applicable to connected networks.</p>
